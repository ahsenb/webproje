var count0 = new Vue({
    el: '#vueCount0',
    data: {
      counter: 0
    },
    methods:{
      increaseCounter(increaseLimit) { // Increase
        if (this.counter < increaseLimit) this.counter++;
      },
      decreaseCounter(decreaseLimit) { // Decrease
        if (this.counter > decreaseLimit) this.counter--;
      }, 
    }
})
